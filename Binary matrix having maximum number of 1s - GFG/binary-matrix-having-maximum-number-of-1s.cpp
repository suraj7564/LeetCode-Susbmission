class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> a, int n) {
        //code here
        int ans = 0, j = n - 1, row = 0;
        for(int i=0;i<n;i++){
            while(a[i][j] == 1 && j >= 0){
                ans++, row = i, j--;
            }
        }
        return {row, ans};
    }
};

