class Solution {
  public:
    int minimizeSum(int n, vector<vector<int>>& t) {
        // Code here
        for(int i=n-2;i>=0;i--){
            for(int j=0;j<t[i].size();j++){
                t[i][j] += min(t[i + 1][j], t[i + 1][j + 1]);
            }
        }
        return t[0][0];
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        vector<vector<int>> triangle;

        for (int i = 0; i < n; i++) {
            vector<int> temp;
            for (int j = 0; j <= i; j++) {
                int x;
                cin >> x;
                temp.push_back(x);
            }
            triangle.push_back(temp);
        }
        Solution obj;
        cout << obj.minimizeSum(n, triangle) << "\n";
    }
    return 0;
}
