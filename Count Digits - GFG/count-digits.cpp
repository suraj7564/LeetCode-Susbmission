class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int cnt = 0, n = N;
        while(N){
            int p = N%10;
            if(p && n%p == 0){
                cnt++;
            }
            N /= 10;
        }
        return cnt;
    }
};

