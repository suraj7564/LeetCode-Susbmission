class Solution{
public:	
	int maxSubstring(string S)
	{
	    // Your code goes here
	    int ans = -2, cur = 0;
	    for(auto x:S){
	        if(x == '0') cur += 1;
	        else cur -= 1;
	        ans = max(ans, cur);
	        cur = max(0, cur);
	    }
	    return ans;
	}
};

