class Solution {
  public:
    int minLaptops(int n, int s[], int e[]) {
        // Code here
        sort(s, s+n);
        sort(e, e+n);
        int cnt = 1, j = 0;
        for(int i=1;i<n;i++){
            if(s[i] < e[j]){
                cnt++;
            }
            else{
                j++;
            }
        }
        return cnt;
    }
};

