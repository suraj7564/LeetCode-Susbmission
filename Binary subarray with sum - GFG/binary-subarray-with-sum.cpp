class Solution{
  public:
    int numberOfSubarrays(vector<int>& a, int n, int k){
        // code here
        map<int, int> m;
        int sum = 0, ans = 0;
        m[0] = 1;
        for(int i=0;i<n;i++){
            sum += a[i];
            ans += m[sum - k];
            m[sum]++;
        }
        return ans;
    }
};

