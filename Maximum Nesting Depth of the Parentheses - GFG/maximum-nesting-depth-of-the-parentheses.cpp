class Solution {
  public:
    int maxDepth(string s) {
        // code here
        int mx = 0, cnt = 0;
        for(auto x:s){
            if(x == '(') cnt++;
            if(x == ')') cnt--;
            mx = max(mx, cnt);
        }
        return mx;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        string s;
        cin >> s;

        Solution obj;
        int ans = obj.maxDepth(s);
        cout << ans << endl;
    }
    return 0;
}
