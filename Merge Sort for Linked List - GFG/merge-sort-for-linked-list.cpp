class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        Node *ans=new Node(-1);
        Node *cur=ans;
        Node *t=head;
        vector<int> v;
        while(t){
             v.push_back(t->data);
             t=t->next;
        }
        sort(v.begin(),v.end());
        for(auto x:v){
            Node *temp=new Node(x);
            cur->next=temp;
            cur=cur->next;
        }
        return ans->next;
    }
};


