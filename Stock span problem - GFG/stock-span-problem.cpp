class Solution
{
    public:
    //Function to calculate the span of stockâ€™s price for all n days.
    vector <int> calculateSpan(int price[], int n)
    {
       // Your code here
       vector<int> ans(n);
       stack<int> s1;
       for(int i=0;i<n;i++){
           while(!s1.empty() && price[s1.top()] <= price[i]){
               s1.pop();
           }
           if(!s1.empty()){
               ans[i] = i - s1.top();
           }
           else ans[i] = i + 1;
           s1.push(i);
       }
       return ans;
    }
};



