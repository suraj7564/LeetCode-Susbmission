class Solution{
    public:
    double MedianOfArrays(vector<int>& arr1, vector<int>& arr2)
    {
        vector<int> cur;
        for(auto x:arr1) cur.push_back(x);
        for(auto x:arr2) cur.push_back(x);
        sort(cur.begin(),cur.end());
        int n=cur.size();
        if(n%2) return cur[n/2];
        return ((double)cur[n/2-1]+(double)cur[n/2])/2.0;
    }
};

