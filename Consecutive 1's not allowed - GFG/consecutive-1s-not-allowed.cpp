//User function template for C++
class Solution{
public:
	// #define ll long long
	ll countStrings(int n) {
	    // code here
	    ll a = 0, b = 1, mod = 1e9+7;
	    for(int i=0;i<=n;i++){
	        ll c = (a + b)%mod;
	        a = b;
	        b = c;
	    }
	    return b;
	}
};

